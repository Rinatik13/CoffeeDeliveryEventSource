package com.example.testdelivery.entity;

import java.math.BigDecimal;

public class Product {
    private int id;

    private BigDecimal price;

    public Product() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }
}
